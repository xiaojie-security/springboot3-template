    /**
     * 向 HttpServletResponse 写入响应体（使用 Result 对象）。
     * <p>
     * 该方法会将 Result 对象序列化为 JSON 字符串，设置响应头为 application/json，
     * 并写入响应输出流。
     * </p>
     *
     * @param result 响应结果对象
     * @throws RuntimeException 写入响应失败时抛出（包装 IOException）
     */
    public void writeResponseBody(Result result) {
        writeResponseBody(result, HttpStatus.OK.value());
    }

    /**
     * 向 HttpServletResponse 写入响应体（使用 Result 对象，可自定义 HTTP 状态码）。
     *
     * @param result     响应结果对象
     * @param httpStatus HTTP 状态码（如 200、401、500 等）
     */
    public void writeResponseBody(Result result, int httpStatus) {
        if (response == null) {
            log.error("HttpServletUtils.writeResponseBody HttpServletResponse对象为空");
            throw new IllegalStateException("HttpServletResponse对象为空");
        }

        if (response.isCommitted()) {
            log.warn("HttpServletUtils.writeResponseBody 响应已提交，无法写入, uri={}",
                    request != null ? request.getRequestURI() : "unknown");
            return;
        }

        try {
            String json = JSONUtil.toJsonStr(result);
            response.setContentType(CONTENT_TYPE);
            response.setCharacterEncoding(CHARSET);
            response.setStatus(httpStatus);
            response.setContentLength(json.getBytes(StandardCharsets.UTF_8).length);

            response.getWriter().write(json);
            response.getWriter().flush();

            log.debug("HttpServletUtils.writeResponseBody 写入响应成功, status={}, code={}, uri={}",
                    httpStatus, result.getCode(), request != null ? request.getRequestURI() : "/");
        } catch (IOException e) {
            log.error("HttpServletUtils.writeResponseBody 写入响应失败, status={}, error={}",
                    httpStatus, e.getMessage(), e);
            throw new RuntimeException("写入响应失败", e);
        }
    }


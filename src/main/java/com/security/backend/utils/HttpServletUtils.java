    /**
     * 校验 User-Agent 是否合法
     *
     * @param userAgent User-Agent 字符串
     * @return true-合法，false-不合法
     */
    public static boolean isValidUserAgent(String userAgent) {
        // 1. 空值校验
        if (userAgent == null || userAgent.isEmpty() || DEFAULT_UNKNOWN.equals(userAgent)) {
            log.warn("HttpServletUtils.isValidUserAgent User-Agent为空或未知, userAgent={}", userAgent);
            return false;
        }

        // 2. 长度校验
        int length = userAgent.length();
        if (length < UA_MIN_LENGTH || length > UA_MAX_LENGTH) {
            log.warn("HttpServletUtils.isValidUserAgent User-Agent长度不合法, length={}, userAgent={}", length, userAgent);
            return false;
        }

        // 3. 黑名单校验（恶意爬虫/扫描器）
        String lowerUA = userAgent.toLowerCase();
        for (String keyword : UA_BLACKLIST_KEYWORDS) {
            if (lowerUA.contains(keyword)) {
                log.warn("HttpServletUtils.isValidUserAgent User-Agent命中黑名单, keyword={}, userAgent={}", keyword, userAgent);
                return false;
            }
        }

        // 4. 包含非法字符校验（只允许可见字符）
        for (char c : userAgent.toCharArray()) {
            if (c < 32 || c > 126) {
                log.warn("HttpServletUtils.isValidUserAgent User-Agent包含非法字符, illegalChar={}, userAgent={}", c, userAgent);
                return false;
            }
        }

        return true;
    }


    /**
     * 截断请求体用于日志输出
     *
     * @param body 请求体
     * @return 截断后的字符串
     */
    private String truncateBody(String body) {
        if (body == null) {
            return null;
        }
        int maxLength = 200;
        if (body.length() <= maxLength) {
            return body;
        }
        return body.substring(0, maxLength) + "...(truncated)";
    }

    /**
     * 安全获取请求属性，避免空指针
     */
    public <T> T getAttributeSafe(String key, Class<T> targetType) {
        if (key == null || targetType == null) {
            log.warn("HttpServletUtils.getAttributeSafe 参数不能为 null");
            return null;
        }

        try {
            Object value = request.getAttribute(key);
            if (value == null) {
                return null;
            }

            // 直接匹配
            if (targetType.isInstance(value)) {
                return targetType.cast(value);
            }

            // 字符串转换
            if (targetType == String.class) {
                return targetType.cast(value.toString());
            }

            // 数字类型转换
            if (Number.class.isAssignableFrom(targetType)) {
                return convertToNumber(value, targetType);
            }

            // Boolean 转换
            if (targetType == Boolean.class) {
                return targetType.cast(convertToBoolean(value));
            }

            // 枚举转换（可选）
            if (targetType.isEnum()) {
                return convertToEnum(value, targetType);
            }

            log.warn("HttpServletUtils.getAttributeSafe 不支持的类型转换: {} -> {}",
                    value.getClass().getSimpleName(), targetType.getSimpleName());
            return null;

        } catch (Exception e) {
            log.warn("HttpServletUtils.getAttributeSafe 获取属性失败, key: {}, error: {}",
                    key, e.getMessage(), e);
            return null;
        }
    }

    @SuppressWarnings("unchecked")
    private <T> T convertToNumber(Object value, Class<T> targetType) {
        Number number;
        if (value instanceof Number) {
            number = (Number) value;
        } else if (value instanceof String) {
            try {
                number = new BigDecimal((String) value);
            } catch (NumberFormatException e) {
                throw new IllegalArgumentException("无法转换为数字: " + value);
            }
        } else {
            throw new IllegalArgumentException("不支持的数字类型: " + value.getClass());
        }

        if (targetType == Integer.class || targetType == Integer.TYPE) {
            return (T) Integer.valueOf(number.intValue());
        }
        if (targetType == Long.class || targetType == Long.TYPE) {
            return (T) Long.valueOf(number.longValue());
        }
        if (targetType == Double.class || targetType == Double.TYPE) {
            return (T) Double.valueOf(number.doubleValue());
        }
        if (targetType == Float.class || targetType == Float.TYPE) {
            return (T) Float.valueOf(number.floatValue());
        }
        if (targetType == Short.class || targetType == Short.TYPE) {
            return (T) Short.valueOf(number.shortValue());
        }
        if (targetType == Byte.class || targetType == Byte.TYPE) {
            return (T) Byte.valueOf(number.byteValue());
        }
        throw new IllegalArgumentException("不支持的数字类型: " + targetType);
    }

    private boolean convertToBoolean(Object value) {
        if (value instanceof Boolean) {
            return (Boolean) value;
        }
        if (value instanceof String) {
            String str = ((String) value).toLowerCase();
            return "true".equals(str) || "1".equals(str) || "yes".equals(str);
        }
        if (value instanceof Number) {
            return ((Number) value).intValue() != 0;
        }
        throw new IllegalArgumentException("无法转换为Boolean: " + value);
    }

    @SuppressWarnings("unchecked")
    private <T> T convertToEnum(Object value, Class<T> enumType) {
        if (value instanceof String) {
            try {
                return (T) Enum.valueOf((Class<Enum>) enumType, (String) value);
            } catch (IllegalArgumentException e) {
                // 尝试按 ordinal 转换
                try {
                    int ordinal = Integer.parseInt((String) value);
                    Enum<?>[] constants = (Enum<?>[]) enumType.getEnumConstants();
                    if (ordinal >= 0 && ordinal < constants.length) {
                        return (T) constants[ordinal];
                    }
                } catch (NumberFormatException ignored) {
                    // 忽略
                }
                throw new IllegalArgumentException("无法转换为枚举: " + value);
            }
        }
        throw new IllegalArgumentException("无法转换为枚举: " + value);
    }

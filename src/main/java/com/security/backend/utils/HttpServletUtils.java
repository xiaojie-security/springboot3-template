    /**
     * 校验 IP 地址是否为内网地址
     *
     * @param ip IP 地址字符串
     * @return true-内网地址，false-公网地址
     */
    public static boolean isPrivateIp(String ip) {
        if (ip == null || ip.isEmpty()) {
            log.warn("HttpServletUtils.isPrivateIp IP地址为空, ip={}", ip);
            return false;
        }

        // 127.0.0.1
        if (ip.startsWith("127.")) {
            return true;
        }

        // 10.0.0.0/8
        if (ip.startsWith("10.")) {
            return true;
        }

        // 172.16.0.0/12
        if (ip.startsWith("172.")) {
            String[] parts = ip.split("\\.");
            if (parts.length == 4) {
                try {
                    int second = Integer.parseInt(parts[1]);
                    if (second >= 16 && second <= 31) {
                        return true;
                    }
                } catch (NumberFormatException e) {
                    log.warn("HttpServletUtils.isPrivateIp 解析IP段异常, ip={}, error={}", ip, e.getMessage());
                }
            }
        }

        // 192.168.0.0/16
        if (ip.startsWith("192.168.")) {
            return true;
        }

        // IPv6 内网地址
        if (ip.startsWith("fe80:") || ip.startsWith("fc") || ip.startsWith("fd")) {
            return true;
        }

        return false;
    }


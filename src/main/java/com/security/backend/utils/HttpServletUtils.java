    /**
     * 解析字符串为设备类型枚举
     *
     * @param value 设备类型字符串
     * @return 设备类型枚举
     */
    private DeviceType parseDeviceType(String value) {
        if (value == null || value.isEmpty()) {
            return DeviceType.UNKNOWN;
        }

        String upperValue = value.toUpperCase();
        for (DeviceType type : DeviceType.values()) {
            if (type.getValue().equals(upperValue) || type.name().equals(upperValue)) {
                return type;
            }
        }

        // 兼容小写或其他常见写法
        String lowerValue = value.toLowerCase();
        if (lowerValue.contains("mobile")) {
            return DeviceType.MOBILE;
        }
        if (lowerValue.contains("tablet") || lowerValue.contains("pad")) {
            return DeviceType.TABLET;
        }
        if (lowerValue.contains("pc") || lowerValue.contains("windows") ||
                lowerValue.contains("mac") || lowerValue.contains("linux")) {
            return DeviceType.PC;
        }

        return DeviceType.UNKNOWN;
    }

